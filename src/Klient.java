public class Klient {
    String Imie;
    String Nazwisko;
    String Pesel;
    String NIP;
    String Ulica;
    String KodPocztowy;
    String Miejscowosc;

    public Klient(String Imie, String Nazwisko, String Pesel, String NIP, String Ulica, String KodPocztowy, String Miejscowosc) {
        this.Imie = Imie;
        this.Nazwisko = Nazwisko;
        this.Pesel = Pesel;
        this.NIP = NIP;
        this.Ulica = Ulica;
        this.KodPocztowy = KodPocztowy;
        this.Miejscowosc = Miejscowosc;
    }
}
